    if (option == "RandomColors") {
        for (var i = 0; i < 200; i++) {
            var _r = getRandomInt(0, 255);
            var _g = getRandomInt(0, 255);
            var _b = getRandomInt(0, 255);
            var clr = "rgb(" + _r + "," + _g + "," + _b + ")";
            RandomColors.push(clr);
        }
        r = RandomColors;
    }
    if (option == "SubtleRandomColors") {
        for (var i = 0; i < 200; i++) {
            var _h = getRandomInt(0, 360);
            var _s = getRandomInt(0, 70);
            var _l = getRandomInt(40, 100);
            var rgb = hsl2rgb(_h, _s, _l);
            var clr = "rgb(" + rgb.r + "," + rgb.b + "," + rgb.g + ")";
            SubtleRandomColors.push(clr);
        }
        r = SubtleRandomColors;
    }
    ac_flag = 0;
    startIndex = previousIndex = getRandomInt(0, r.length - 1);
    if (option == "AdobeColors") {
        startIndex = previousIndex = getRandomInt(1, r.length - 1);
        ac_flag = 1;
    }
